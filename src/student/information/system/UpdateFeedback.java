public void update(int id, String date, String feedback, String discription) {
        String sql = "UPDATE feedbackdata SET date = ?, feedback = ?, discription = ? WHERE id = ?";

        try (Connection conn = DatabaseConnection.ConnectDb();
                PreparedStatement pstmt = conn.prepareStatement(sql)) {

            // set the corresponding param
            pstmt.setString(1, date);
            pstmt.setString(2, feedback);
            pstmt.setString(3, discription);
            pstmt.setInt(4, id);
            // update 
            pstmt.executeUpdate();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
